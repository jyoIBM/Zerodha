package Method;

public class Staticcallfromsameclass {//class body open
public static void main(String[]args) {//method body open
	za();
	zb();
	zc();
	zd();
	
}//method body close

public static void za(){//regular method body open
	System.out.println("execute za");
}//regular method body close

public static void zb() {
	System.out.println("execute zb");
}
public static void zc() {
	System.out.println("execute zc");
}
public static void zd() {
	System.out.println("execute zd");
}

}//class body close	
	
	
	



